package org.wso2.carbon.apimgt.hostobjects.oidc.internal;

public class AuthClient {

    String clientId;
    String clientSecret;
    String authorizationType;
    String redirectURI;
    String clientAlgorithm;

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getClientSecret() {
        return clientSecret;
    }

    public void setClientSecret(String clientSecret) {
        this.clientSecret = clientSecret;
    }

    public String getAuthorizationType() {
        return authorizationType;
    }

    public void setAuthorizationType(String authorizationType) {
        this.authorizationType = authorizationType;
    }

    public String getRedirectURI() {
        return redirectURI;
    }

    public void setRedirectURI(String redirectURI) {
        this.redirectURI = redirectURI;
    }

    public String getClientAlgorithm() {
        return clientAlgorithm;
    }

    public void setClientAlgorithm(String clientAlgorithm) {
        this.clientAlgorithm = clientAlgorithm;
    }


}
